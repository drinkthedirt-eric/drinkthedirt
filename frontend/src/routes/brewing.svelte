<div class="font-bold text-4xl pb-2">Understanding Our Recipes</div>
<div>It’s simple enough to pour hot water through ground coffee and a paper filter, but getting the best flavor out of your brews can quickly get complicated. Read on to get all the details on making great filter coffee.</div>
<br>
<div class="font-medium text-2xl pb-2">Coffee Extraction</div>
<div class="pb-2">In coffee brewing, extraction refers to the flavors and compounds we pull out from coffee grounds into the resulting coffee drink. In general, there’s a sweet spot in extraction that balances acidity and sweetness while minimizing bitterness.</div>
<div>Coffees that are under extracted will taste acidic to the point of sourness. Over extracted coffees will taste bitter or astringent. Below you can read about the variables that impact extraction, and how to adjust them to make your perfect cup.</div>
<br>
<div class="font-medium text-2xl pb-2">Extraction Variables</div>
<div class="text-xl pb-1">Temperature</div>
<div class="pb-2">Usually, between 93C and 99C. Hotter water increases extraction, we recommend brewing at 97C and changing other variables first.</div>
<div class="text-xl pb-1">Grind Size</div>
<div class="pb-2">For pour over we grind between medium-fine and medium, which is grind #4 to grind #10 on our grind scale. Grinder finer will increase extraction, and grinder courser will decrease extraction.</div>
<div class="text-xl pb-1">Coffee: Water Ratio</div>
<div class="pb-2">Usually we brew between 1:13 and 1:17. This ratio refers to the amount of water you add to ground coffee. A ratio of 1:16 means using 16g for every 1g of coffee in your brew. Longer ratios can increase extraction - sometimes it makes the coffee watered-down.</div>
<div class="text-xl pb-1">Agitation</div>
<div class="pb-2">We agitate the coffee bed with stirring and pouring. We like to adjust our pouring technique over adjusting our stirring. Extraction will increase with higher pouring and more frequent pours.</div>
